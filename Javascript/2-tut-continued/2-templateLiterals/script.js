const name = "Sandhya";
const age = 18;

// Traditional concatenation
const sentence = "Hey it's " + name + " and she is " + age + " years old.";
console.log("Traditional concatenation:", sentence);

// Template Literal (using backticks and interpolation)
const value = `Hey it's ${name} and she is ${age} years old.`;
console.log("Template Literal:", value);
