// Switch Statement
let num = 2;
switch (num) {
  case 1:
    console.log("One");
    break;
  case 2:
    console.log("Two");
    break;
  case 3:
    console.log("Three");
    break;
  default:
    console.log("Not found");
}

// Loop

// While loop
num = 10;
while (num > 0) {
  console.log(num);
  num--;
}

// Do While loop
num = 10;
do {
  console.log(num);
  num--;
} while (num > 0);

// for loop
for (let i = 0; i < 10; i++) {
  console.log(i);
}
